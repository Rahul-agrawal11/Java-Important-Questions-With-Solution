class Result {
    public static void main(String[] args) {
        float a = 7/4*9/2;
        /*
            = 1*9/2
            = 9/2
            = 4.0
        */
       System.out.println("Result is: "+a);

       float b = 7/4.0f*9/2.0f;
       /*
            = 1.75*9/2
            = 15.75/2
            = 7.875
        */
       System.out.println("Result is: "+b);

    }
}