class EscapeSequenceCharacter{
    public static void main(String[] args) {
        //  [/n , /t, /", // etc.]

        System.out.println("Hi, I am \n Rahul."); // new line
        System.out.println("My full name is \tRahul Agrawal"); // return sapce as tab
        System.out.println("My full name is \"Rahul Agrawal\""); // for use double quotation in the statement.
        System.out.println("My full name is \\Rahul Agrawal"); // for use slash in the statement.
    }
}